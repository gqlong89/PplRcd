HMI *CreateHMI( int hmiType)
{
	HMI *p_hmi = NULL;
	switch( hmiType)
	{
		case HMI_MAIN:
			if( p_mainHmi == NULL)
			{
				p_mainHmi = mainHmi_new();
			}
			p_hmi = SUPER_PTR( p_mainHmi, HMI);
			break; 
		case HMI_CMM:
			
			p_hmi = SUPER_PTR(  GetCmmHMI(), HMI);
			break; 
		case HMI_KYBRD:
			
			p_hmi = SUPER_PTR(  GetkeyboardHMI(), HMI);
			break; 
		default:
			break;
	}
	
	return p_hmi;
}

